package com.bidetti.bootcamp.metodos;

public class Calculadora {

    public static void soma(double a, double b) {
        System.out.println("A soma entre: " + a + " + " + b + " é " + (a + b));
    }

    public static void subtracao(double a, double b) {
        System.out.println("A subtracao entre: " + a + " - " + b + " é " + (a - b));
    }

    public static void multiplicacao(double a, double b) {
        System.out.println("A multiplicacao entre: " + a + " * " + b + " é " + (a * b));
    }

    public static void divisao(double a, double b) {
        System.out.println("A divisao entre: " + a + " / " + b + " é " + (a / b));
    }
}
